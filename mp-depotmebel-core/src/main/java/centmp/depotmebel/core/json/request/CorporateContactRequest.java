package centmp.depotmebel.core.json.request;

public class CorporateContactRequest {

	private String label;
	private String picName;
	private String picPosition;
	private String picDepartment;
	private String picPhone;
	private String picEmail;
	private String isDefaultDelivery;
	private String isBilling;
	private String address;
	private String latitude;
	private String longitude;
	private String cityId;
	private String cityName;
	
	public String getLabel() {
		return label;
	}
	public void setLabel(String label) {
		this.label = label;
	}
	public String getPicName() {
		return picName;
	}
	public void setPicName(String picName) {
		this.picName = picName;
	}
	public String getPicPosition() {
		return picPosition;
	}
	public void setPicPosition(String picPosition) {
		this.picPosition = picPosition;
	}
	public String getPicDepartment() {
		return picDepartment;
	}
	public void setPicDepartment(String picDepartment) {
		this.picDepartment = picDepartment;
	}
	public String getPicPhone() {
		return picPhone;
	}
	public void setPicPhone(String picPhone) {
		this.picPhone = picPhone;
	}
	public String getPicEmail() {
		return picEmail;
	}
	public void setPicEmail(String picEmail) {
		this.picEmail = picEmail;
	}
	public String getIsDefaultDelivery() {
		return isDefaultDelivery;
	}
	public void setIsDefaultDelivery(String isDefaultDelivery) {
		this.isDefaultDelivery = isDefaultDelivery;
	}
	public String getIsBilling() {
		return isBilling;
	}
	public void setIsBilling(String isBilling) {
		this.isBilling = isBilling;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getLatitude() {
		return latitude;
	}
	public void setLatitude(String latitude) {
		this.latitude = latitude;
	}
	public String getLongitude() {
		return longitude;
	}
	public void setLongitude(String longitude) {
		this.longitude = longitude;
	}
	public String getCityId() {
		return cityId;
	}
	public void setCityId(String cityId) {
		this.cityId = cityId;
	}
	public String getCityName() {
		return cityName;
	}
	public void setCityName(String cityName) {
		this.cityName = cityName;
	}
	
}
